#include "libft.h"

void	ft_putnbr(int n)
{
	long	i;

	i = n;
	if (n < 0)
	{
		ft_putchar('-');
		i = -i;
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
		ft_putchar(i + '0');
}
